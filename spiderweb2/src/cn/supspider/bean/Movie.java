package cn.supspider.bean;

public class Movie {
	private int number;
	private String M_name;
	private String M_size;
	private String M_type;
	private String M_intotime;
	private int M_count;
	private String M_from;
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getM_name() {
		return M_name;
	}
	public void setM_name(String m_name) {
		M_name = m_name;
	}
	public String getM_size() {
		return M_size;
	}
	public void setM_size(String m_size) {
		M_size = m_size;
	}
	public String getM_type() {
		return M_type;
	}
	public void setM_type(String m_type) {
		M_type = m_type;
	}
	public String getM_intotime() {
		return M_intotime;
	}
	public void setM_intotime(String m_intotime) {
		M_intotime = m_intotime;
	}
	public int getM_count() {
		return M_count;
	}
	public void setM_count(int m_count) {
		M_count = m_count;
	}
	public String getM_from() {
		return M_from;
	}
	public void setM_from(String m_from) {
		M_from = m_from;
	}
}
