package cn.supspider.bean;

public class ResourceAll {
	private int number;
	private String R_name;
	private String R_size;
	private String R_type;
	private String R_intotime;
	private int R_count;
	private String R_from;
	private String R_link;
	
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getR_name() {
		return R_name;
	}
	public void setR_name(String r_name) {
		R_name = r_name;
	}
	public String getR_size() {
		return R_size;
	}
	public void setR_size(String r_size) {
		R_size = r_size;
	}
	public String getR_type() {
		return R_type;
	}
	public void setR_type(String r_type) {
		R_type = r_type;
	}
	public String getR_intotime() {
		return R_intotime;
	}
	public void setR_intotime(String r_intotime) {
		R_intotime = r_intotime;
	}
	public int getR_count() {
		return R_count;
	}
	public void setR_count(int r_count) {
		R_count = r_count;
	}
	public String getR_from() {
		return R_from;
	}
	public void setR_from(String r_from) {
		R_from = r_from;
	}
	public String getR_link() {
		return R_link;
	}
	public void setR_link(String r_link) {
		R_link = r_link;
	}

}
